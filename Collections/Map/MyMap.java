/* Mapping: A Map defines mapping from keys to values, the key value pairs is an entry. A Map does not allow duplicate keys
that means keys are unique. each key map to one value, which is called single value map.

Both keys and value must be object. A Map is not a collection and Map interface does not extend Collection interface.
*/

